## --------- http request types: get, put, post, delete--------------##
## requests.get() -- when we ask a external system for a particular piece of data and the system provides the data in
##                   response.... eg. using APIs
## requests.post() -- when we give the external system a piece of data and are not bothered much abt response except...
##                   whether it was succssful or not ... Eg. saving data in google spreadsheets, post a post on insta
## requests.put() -- when we update an existing piece of data in external system... eg. updating google sheets
## requests.delete() -- when we want to delete a data in external systaem... eg. deleting a post on insta